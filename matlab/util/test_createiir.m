%% Simple test file for createiir method

% Quantity of steps
N = 1001;

% System poles
poles = [0.91*exp(j*2*pi*0.10*[-1 1]) ...
    0.50*exp(j*2*pi*0.125*[-1 1]) ...
    0.69*exp(j*2*pi*0.15*[-1 1]) ...
    0.68*exp(j*2*pi*0.20*[-1 1]) ...
    0.72*exp(j*2*pi*0.23*[-1 1]) ...
    0.94*exp(j*2*pi*0.26*[-1 1]) ...
    ];

% System zeros
zeros = [1.00*exp(j*2*pi*0.00*[-1 1]) ...
    0.36*exp(j*2*pi*0.02*[-1 1]) ...
    0.90*exp(j*2*pi*0.35*[-1 1]) ...
    ];

% X and Y filter cutoffs
x_specs = [0 0 0.04 0.04; ...
          0.1 0.1 0.25 0.25; ...
          0.32 0.32 0.5 0.5;];
y_specs = [-40 -120 -120 -40; ...
           -5 -7 -2 0; ...
           -35 -120 -120 -35;];

% Run createiir method
createiir(poles, zeros, x_specs, y_specs, N);